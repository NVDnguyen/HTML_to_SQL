    private void jFileChooser2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser2ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
         fileChooser.setCurrentDirectory(FileSystemView.getFileSystemView().getHomeDirectory());
        // Hiển thị JFileChooser và kiểm tra xem người dùng đã chọn tệp hay không
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            if (selectedFile != null) {
                // Người dùng đã chọn một tệp, lấy đường dẫn của nó và thực hiện các hành động cần thiết
                String selectedFilePath = selectedFile.getAbsolutePath();
                jTextField1.setText(selectedFilePath);
                jPanel2.setVisible(false);
                jPanel1.setVisible(true);
                pack();
                // Thực hiện các hành động khác dựa trên đường dẫn đã chọn
                // Ví dụ: hiển thị đường dẫn trong một JTextField hoặc thực hiện xử lý tệp
            }
        } else if (result == JFileChooser.CANCEL_OPTION) {
            // Người dùng đã hủy bỏ hành động chọn tệp
            System.out.println("Open command cancelled by user.");
        }

    }//GEN-LAST:event_jFileChooser2ActionPerformed

