    private void enterFilePathActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterFilePathActionPerformed
        StringBuilder result = new StringBuilder();
        StringBuilder output = new StringBuilder();
        String html = fet.readHtmlFromFile(jTextField2.getText());
        if (!html.isEmpty()) {
            List<Questions> test = fet.extractData(html);
            result.append("INSERT INTO Questions ( descriptionQ, answer1, answer2, answer3, answer4, CorrectAnswer, idTest) VALUES");
            output.append("INSERT INTO Questions ( descriptionQ, answer1, answer2, answer3, answer4, CorrectAnswer, idTest) VALUES \n");
            for (Questions questions : test) {
                result.append(questions.generateInsertStatement(jTextField5.getText()));
                output.append(questions.generateInsertStatement(jTextField5.getText())).append("\n");
            }
            clip = output.toString();
            jTextArea1.setText(output.toString());
        } else {
            jLabel2.setText("html empty or somthing errored");

        }

    }//GEN-LAST:event_enterFilePathActionPerformed

    private void urlButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_urlButtonActionPerformed
        jLabel2.setText("");
        StringBuilder result = new StringBuilder();
        StringBuilder output = new StringBuilder();
        String html = "";
        if (fet.isValidUrl(jTextField2.getText())) {
            try {
                html = fet.getHtmlFromUrl(jTextField2.getText());
                jTextArea1.setText(html);
            } catch (Exception e) {
                jLabel2.setText("something wrong with url !!!");
            }
            if (!html.isEmpty()) {
                List<Questions> test = new ArrayList<>();
                try {
                    test = fet.extractData(html);
                    result.append("INSERT INTO Questions ( descriptionQ, answer1, answer2, answer3, answer4, CorrectAnswer, idTest) VALUES");
                    output.append("INSERT INTO Questions ( descriptionQ, answer1, answer2, answer3, answer4, CorrectAnswer, idTest) VALUES \n");
                    for (Questions questions : test) {
                        result.append(questions.generateInsertStatement(jTextField5.getText()));
                        output.append(questions.generateInsertStatement(jTextField5.getText())).append("\n");
                    }
                    clip = output.toString();
                    jTextArea1.setText(output.toString());

                } catch (Exception e) {
                    jLabel2.setText("something wrong with extractData !!!");
                }

            } else {
                jLabel2.setText("html empty");
            }
        } else {
            jLabel2.setText("cannot connect with this url");

        }

    }//GEN-LAST:event_urlButtonActionPerformed

